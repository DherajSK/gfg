class Solution
{
    public:
    //Function to find position of first set bit in the given number.
    unsigned int getFirstSetBit(int n)
    {
        int power2=1,count=0;
        while(power2<=n)
        {
            if((n&power2)==power2)
                return count+1;
            count++;
            power2*=2;
        }
        return count;
    
    }
};

